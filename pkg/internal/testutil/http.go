package testutil

import (
	"net"
	"strconv"
)

// SuggestPort suggest an unused port
func SuggestPort() string {
	addr, err := net.ResolveTCPAddr("tcp", "localhost:0")
	if err != nil {
		panic(err)
	}

	l, err := net.ListenTCP("tcp", addr)
	if err != nil {
		panic(err)
	}

	// nolint:errcheck
	defer l.Close()

	return strconv.Itoa(l.Addr().(*net.TCPAddr).Port)
}
